package factory;

public class Lampada {
	
	private Lampada cor;
	private Lampada luminacia;
	
	public Lampada() {}

	public Lampada getCor() {
		return cor;
	}

	public void setCor(Lampada lampada) {
		this.cor = lampada;
	}

	public Lampada getLuminacia() {
		return luminacia;
	}

	public void setLuminacia(Lampada luminacia) {
		this.luminacia = luminacia;
	}


}

